// Dimensions:
// Function downsamples canvases to smaller temporary canvas
// Used for image-based dimension estimates
function downsampleToCanvas(srcCanvas, size) {
    const s = Math.max(128, size | 0);

    const off = document.createElement("canvas");
    off.width = s;
    off.height = s;

    // Converts visible canvas -> smaller bitmap
    const octx = off.getContext("2d", { willReadFrequently: true });
    octx.imageSmoothingEnabled = false;     // Keeps hard pixel edges rather than blurring mask
    
    octx.clearRect(0, 0, s, s);
    octx.drawImage(srcCanvas, 0, 0, s, s);
    
    return { octx, size: s };
}

// Builds binary mask from cavnas image data
function makeInkMaskFromImageData(img, w, h, { alphaMin = 16 } = {}) {
    const mask = new Uint8Array(w * h);
    const d = img.data;

    // Iterates through pixels
    for (let i = 0, px = 0; i < d.length; i += 4, px++) {
        const a = d[i + 3];
        mask[px] = a >= alphaMin ? 1 : 0;   // Any pixel with alpha >= alphaMin treated as occupied/ink
    }
    
    return mask;
}

// Function creates boundary mask from occupied mask
function makeBoundaryFromMask(mask, w, h) {
    const out = new Uint8Array(w * h);
    const idx = (x, y) => y * w + x;
    
    // Iterates through pixels
    // Skips outer border to avoid reading outside the array
    for (let y = 1; y < h - 1; y++) {
        for (let x = 1; x < w - 1; x++) {
            const i = idx(x, y);

            // Only occupied pixels can be part of boundary
            if (!mask[i]) continue;
            if (
                !mask[idx(x + 1, y)] || 
                !mask[idx(x - 1, y)] || 
                !mask[idx(x, y + 1)] || 
                !mask[idx(x, y - 1)]
            ) {
                // Pixel is boundary if it occupied & at least one of its direct neighbours is empty
                out[i] = 1;
            }
        }
    }

    return out;
}

function setDimReadout(result) {
    if (!result || !Number.isFinite(result.dimension)) {
        UI.dimValue().textContent = "-";
        UI.dimMeta().textContent = "Could not estimate (try changing box range / samples).";
        
        return;
    }

    const r2 = result.fit?.r2;
    // Updates dimension readout after DimensionLab finishes
    UI.dimValue().textContent = result.dimension.toFixed(4);
    UI.dimMeta().textContent
        = `${result.estimator}${Number.isFinite(r2) ? `, \\(R^2=${r2.toFixed(3)}\\)` : ""}`;
    typesetMath(UI.dimMeta());
}

// Function runs box-counting dimension estimation on current dendrite drawing
function runDimensionEstimate() {
    UI.dimValue().textContent = "...";
    UI.dimMeta().textContent = "Estimating...";

    // Reads estimator options from UI
    const ds = Math.max(128, parseNumber(UI.dimDownsample().value, 768) | 0);
    const minBox = Math.max(1, parseNumber(UI.dimMinBox().value, 2) | 0);
    const maxBox = Math.max(minBox + 1, parseNumber(UI.dimMaxBox().value, 256) | 0);
    const samples = Math.max(6, parseNumber(UI.dimSamples().value, 12) | 0);

    // Converts visible canvas -> smaller bitmap
    const { octx, size } = downsampleToCanvas(canvas, ds);
    const img = octx.getImageData(0, 0, size, size);

    // Ink mask marks occupied pixels
    const ink = makeInkMaskFromImageData(img, size, size, { alphaMin: 16 });
    // Chooses all visible ink OR only boundary pixels
    const bitmap = (UI.dimMode().value === "boundary") 
        ? makeBoundaryFromMask(ink, size, size) 
        : ink;

    try {
        const result = DimensionLab.run({
            estimator: "boxCountBitmap",
            input: {
                kind: "bitmap",
                data: { mask: bitmap, width: size, height: size },
                meta: { page: "affine" }
            },
            options: { minBox, maxBox, samples },
        });

        setDimReadout(result);
    } catch (err) {
        console.error(err);

        UI.dimValue().textContent = "-";
        UI.dimMeta().textContent = `Dimension error: ${err.message}`;
    }
}

UI.dimRunBtn()?.addEventListener("click", runDimensionEstimate);

// Animation loop function for visible Chaos-Game stepping
function animLoop() {
    if (!playing) return;

    // Adds several Chaos Game steps per frame s.t. animation visibly progresses
    stepMany(24);
    render();

    rafId = requestAnimationFrame(animLoop);
}

function stopAnim() {
    playing = false;

    if (rafId) {
        // Stops step animation
        cancelAnimationFrame(rafId);
    }
    rafId = null;

    // Resets play button label
    if (UI.toggleAnim()) {
        UI.toggleAnim().textContent = "Play";
    }
}

function stepMany(count = 1) {
    for (let i = 0; i < count; i++) {
        const map = chooseMap(affineMaps);

        currentPoint = applyAffine(currentPoint, map);
        // Advances step-by-step Chaos Game by count steps
        stepPoints.push({ ...currentPoint });
    }

    // Keeps only recent step points
    // Avoids endlessly growing overlay
    if (stepPoints.length > 2500) {
        stepPoints.splice(0, stepPoints.length - 2500);
    }
}

function stepOnce() {
    // Advances step-by-step Chaos Game once
    stepMany(1);
    render();       // redraws
}

function resetView() {
    // Resets view to default window for active example
    view2d.initial = { ...(exampleViews[activeExampleName] || DEFAULT_VIEW) };
    // Clears step overlay and regeneraets attractor
    view2d.setView(view2d.initial, { trigger: false });
    
    currentPoint = randomSeedPoint();
    stepPoints = [];
    needsRegen = true;

    render();
}

