// Function to render probability editor
function normaliseProbsUI() {
    const wrap = UI.probWrap();
    wrap.replaceChildren();
    
    let sum = 0;
    const mode = UI.probabilityMode()?.value || "custom";
    const isCustom = mode === "custom";

    affineMaps.forEach((m, i) => {
        const row = document.createElement("div");
        row.style.display = "grid";
        row.style.gridTemplateColumns = "1fr auto";
        row.style.gap = "10px";
        row.style.alignItems = "end";
        row.style.marginTop = "10px";

        const p = buildLabeledNumber(`p${i + 1}`, "0.001", m.prob, v => {
            // Manual probability edits switches back to custom mode
            // "custom" = can edit probabilities directly
            // "automatic" = probability fields disabled
            UI.probabilityMode().value = "custom";
            affineMaps[i].prob = Math.max(0, v);

            normaliseProbs();

            needsRegen = true;
            normaliseProbsUI();

            render();
        });

        // Disables probability inputs when probabilities generated automatically
        p.input.disabled = !isCustom;

        const badge = document.createElement("div");
        badge.className = "mono";
        badge.textContent = `||A|| ${fmt(spectralNorm(m), 3)}`;

        row.append(p.wrapper, badge);
        wrap.appendChild(row);

        sum += (+m.prob || 0);
    });

    const summary = document.createElement("div");
    summary.style.marginTop = "12px";
    summary.style.display = "flex";
    summary.style.gap = "10px";
    summary.style.alignItems = "center";

    const sumText = document.createElement("div");
    sumText.className = "mono";
    sumText.innerHTML = `Sum <strong>${sum.toFixed(4)}</strong>`;

    const btn = document.createElement("button");
    btn.type = "button";
    btn.className = "tiny-btn";
    btn.textContent = "Normalise";
    btn.disabled = !isCustom;

    btn.addEventListener("click", () => {
        normaliseProbs();

        needsRegen = true;
        normaliseProbsUI();

        render();
    });

    summary.append(sumText, btn);
    wrap.appendChild(summary);
}

