function applyAffine(p, m) {
    // Applies single affine map to point: f(x,y) = A(x,y) + b
    return { 
        x: m.a11 * p.x + m.a12 * p.y + m.b1, 
        y: m.a21 * p.x + m.a22 * p.y + m.b2 
    };
}

function chooseMap(maps) {
    // Random choice step in Chaos Game
    const r = Math.random();
    let acc = 0;

    // Chooses one affine map using stored probabilities
    for (const m of maps) {
        acc += m.prob;
        if (r <= acc) return m;
    }

    // Falback in case floating-point totals are slightly below 1
    return maps[maps.length - 1];
}

// Function generates IFS attractor using Chaos Game
function generateAffineFractal(maps, n = 10000, x0 = 0, y0 = 0, burnIn = 30) {
    if (!maps?.length) return [];

    const pts = [];
    let p = { x: x0, y: y0 };

    // Burn-in removes early points before orbit settles onto the attractor
    for (let i = 0; i < burnIn; i++) {
        p = applyAffine(p, chooseMap(maps));
    }

    // Main Chaos Game orbit
    for (let i = 0; i < n; i++) {
        p = applyAffine(p, chooseMap(maps));
        pts.push({ x: p.x, y: p.y });
    }

    return pts;
}

function imageParallelogram(m) {
    // Corners of f (REF square):
    const b = { x: m.b1, y: m.b2 };
    const c1 = { x: m.b1 + m.a11, y: m.b2 + m.a21 };
    const c2 = { x: m.b1 + m.a12, y: m.b2 + m.a22 };
    const c12 = { 
        x: m.b1 + m.a11 + m.a12, 
        y: m.b2 + m.a21 + m.a22 
    };

    // Computed image of reference unit square under one affine map
    // Four points are drawn as draggable parallelogram
    return { b, c1, c2, c12 };
}

// Function checks if world-coordinate mouse point is close to any draggable corner of any affine map parallelogram
function pickCorner(worldPt) {
    const r = handleRadiusWorld();
    
    for (let i = 0; i < affineMaps.length; i++) {
        const { b, c1, c2, c12 } = imageParallelogram(affineMaps[i]);

        // Using Euclidean distance function for calculations
        if (dist(worldPt, b) <= r) return { mapIndex: i, corner: "b" };
        if (dist(worldPt, c1) <= r) return { mapIndex: i, corner: "c1" };
        if (dist(worldPt, c2) <= r) return { mapIndex: i, corner: "c2" };
        if (dist(worldPt, c12) <= r) return { mapIndex: i, corner: "c12" };
    }
    
    return null;
}

// Function updates affine map when one of its visual handles is dragged
function applyCornerDrag(mapIndex, corner, newPosWorld, keepWhich = "c1") {
    const m = affineMaps[mapIndex];

    // Current reference square image
    const { b, c1, c2 } = imageParallelogram(m);

    // b changes translation
    if (corner === "b") {
        // Moving b translates whole affine image
        m.b1 = newPosWorld.x;
        m.b2 = newPosWorld.y;
        return;
    }

    // c1 changes first column of A
    if (corner === "c1") {
        // Moving c1 edits first column of A
        m.a11 = newPosWorld.x - b.x;
        m.a21 = newPosWorld.y - b.y;
        return;
    }

    // c2 changes second column of A
    if (corner === "c2") {
        // Moving c2 edits second column of A
        m.a12 = newPosWorld.x - b.x;
        m.a22 = newPosWorld.y - b.y;
        return;
    }

    // c12 changes one column while keeping other fixed
    if (corner === "c12") {
        // Opposite corner: changes one direction while keeping the other fixed
        const sx = newPosWorld.x - b.x;
        const sy = newPosWorld.y - b.y;

        if (keepWhich === "c2") {
            // Keeps c2 fixed and solves for c1
            const c2x = c2.x - b.x;
            const c2y = c2.y - b.y;

            m.a11 = sx - c2x;
            m.a21 = sy - c2y;
        } else {
            // Keeps c1 fixed and solves for c2
            const c1x = c1.x - b.x;
            const c1y = c1.y - b.y;

            m.a12 = sx - c1x;
            m.a22 = sy - c1y;
        }
    }
}

function normaliseProbs() {
    // Adds up all current probailities
    // Treats invalid values as 0
    const sum = affineMaps.reduce((s, m) => s + (+m.prob || 0), 0);

    // If total sum +ve, scales each probability s.t. they add up to 1
    if (sum > 0) {
        affineMaps.forEach(m => (m.prob = (+m.prob || 0) / sum));
    } else {
        // If all probabilities 0 or invalid, gives each map equal probability
      const p = 1 / Math.max(1, affineMaps.length);
      affineMaps.forEach(m => (m.prob = p));
    }
}

// Function applies selected probability mode
function applyProbabilityMode() {
    // "custom" = normalise user values
    const mode = UI.probabilityMode()?.value || "custom";
    if (!affineMaps.length || mode === "custom") {
        normaliseProbs();
        return;
    }

    // "equal" = eap map equally likely
    if (mode === "equal") {
        const p = 1 / affineMaps.length;
        affineMaps.forEach(m => (m.prob = p));
        return;
    }

    // "spectral" = weight maps by approximate matrix size
    if (mode === "spectral") {
        const weights = affineMaps.map(m => {
            const norm = spectralNorm(m);

            return Number.isFinite(norm) ? Math.max(norm, 0.0001) : 0.0001;
        });

        const sum = weights.reduce((s, w) => s + w, 0);
        affineMaps.forEach((m, i) => (m.prob = weights[i] / sum));
    }
}

// Function renders warnings about whetehr each affine map appears contractive
function renderContractionWarnings() {
    const wrap = UI.contractionWarnings();
    if (!wrap) return;
    wrap.replaceChildren();

    if (!affineMaps.length) {
        const empty = document.createElement("div");
        empty.className = "diagnostic";
        empty.textContent = "No maps to check.";
        wrap.appendChild(empty);
        return;
    }

    affineMaps.forEach((m, i) => {
        const norm = spectralNorm(m);
        const item = document.createElement("div");
        item.className = "diagnostic";

        // Catches maps which may blow up instead of forming an attractor
        if (norm >= 1) {
            item.classList.add("danger");
            item.textContent = `Map ${i + 1}: ||A|| approx ${fmt(norm)}. This map may not be contractive.`;
        } else if (norm > 0.5) {
            item.classList.add("warn");
            item.textContent = `Map ${i + 1}: ||A|| approx ${fmt(norm)}. Large contractions can give heavy overlap or filled regions.`;
        } else {
            item.textContent = `Map ${i + 1}: ||A|| approx ${fmt(norm)}. Contractive.`;
        }

        wrap.appendChild(item);
    });
}

function ensureMapsLength(n) {
    // Ensures affineMaps array has exactly n maps
    n = clampInt(Math.floor(n), 1, 10);
    while (affineMaps.length < n) {
        // Adds default maps if user increases map count
        affineMaps.push({ 
            a11: 0.5, a12: 0, 
            a21: 0, a22: 0.5, 
            b1: 0, b2: 0, 
            prob: 1 });
    }

    // Removes maps from end if user decreases map count
    while (affineMaps.length > n) {
        affineMaps.pop();
    }

    applyProbabilityMode();
    needsRegen = true;
}

