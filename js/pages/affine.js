// Clamps integer in interval [a, b]
function clampInt(x, a, b) {
    return Math.max(a, Math.min(b, x | 0));
}
// Function calcualtes Euclidean distance between world-coordinate points
// Used for when mouse is close to draggable hanlde
function dist(p, q) { 
    return Math.hypot(p.x - q.x, p.y - q.y); 
}

// Chaos Game: random starting point
function randomSeedPoint() { 
    return { 
        // Initialising near reference square s.t. orbit settles quickly
        x: Math.random() * 1.4 - 0.2, 
        y: Math.random() * 1.4 - 0.2 
    }; 
}

// Function formats numbers neatly for input boxes and readouts
function fmt(x, places = 4) {
    if (!Number.isFinite(x)) {
        return "0";
    }
    const rounded = Number(x.toFixed(places));

    // Avoids displaying value "-0"
    return Object.is(rounded, -0) ? "0" : String(rounded);
}

// Function used to roughly measure how strongly each affine map contracts space
function spectralNorm(m) {
    // 2 x 2 matrix A
    const a = m.a11;
    const b = m.a12;
    const c = m.a21;
    const d = m.a22;

    // Eigevalue fomrula: A^T A
    const trace = a * a + b * b + c * c + d * d;
    const det = (a * d - b * c) ** 2;
    const disc = Math.max(0, trace * trace - 4 * det);

    // Returns estimated spectral norm of A
    return Math.sqrt(Math.max(0, (trace + Math.sqrt(disc)) / 2));
}

// Converts world coordinates -> canvas pixel coordinates
function worldToCanvas(x, y) {
    const v = view2d.view;
    const u = (x - v.xMin) / (v.xMax - v.xMin);
    const t = (v.yMax - y) / (v.yMax - v.yMin);

    return { 
        px: u * canvas.width, 
        py: t * canvas.height 
    };
}

// Converts mouse/pointer event -> canvas pixel coordinates
function clientToCanvasPx(e) {
    const rect = canvas.getBoundingClientRect();
    const cx = (e.clientX - rect.left) * (canvas.width / rect.width);
    const cy = (e.clientY - rect.top) * (canvas.height / rect.height);

    return { cx, cy };
}

// Converts fixed screen handle radius to world-coordinate size
function handleRadiusWorld() {
    const v = view2d.view;
    const worldW = v.xMax - v.xMin;

    // Keeps draggable handles easy to select at different zoom levels
    return (12 / canvas.width) * worldW;
}

// Function chooses clean grid step near x
function niceStep(x) {
    const p = Math.pow(10, Math.floor(Math.log10(x)));
    const a = x / p;

    // Ensures its a 'nice' step, e.g. 0.02 and not 0.037
    if (a < 1.5) return 1 * p;
    if (a < 3.5) return 2 * p;
    if (a < 7.5) return 5 * p;

    return 10 * p;
}

function ink(alpha = 0.95) {
    const inkVar = getComputedStyle(document.documentElement)
        .getPropertyValue("--ink")
        .trim();
    
    if (inkVar.startsWith("rgb(")) {
        return inkVar.replace("rgb(", "rgba(").replace(")", `,${alpha})`);  // Adds alpha transparency to rgb() colours
    }

    if (inkVar) {
        return inkVar;  // fallback (no alpha)
    }

    // Returns current theme ink colour
    return `rgba(20,20,20,${alpha})`;
}

