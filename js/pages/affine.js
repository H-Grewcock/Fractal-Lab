// Function for drawing grid in current view
function drawGrid() {
    const v = view2d.view;
    const wWorld = v.xMax - v.xMin;
    // Picks nice grid spacing
    const step = niceStep(wWorld / 10);

    ctx.save();

    ctx.lineWidth = 1;
    ctx.strokeStyle = "rgba(0,0,0,0.08)";

    // Outer grid lines still visible in current view
    const xStart = Math.floor(v.xMin / step) * step;
    const xEnd = Math.ceil(v.xMax / step) * step;
    const yStart = Math.floor(v.yMin / step) * step;
    const yEnd = Math.ceil(v.yMax / step) * step;

    // Vertical grid lines
    for (let x = xStart; x <= xEnd; x += step) {
        const { px } = worldToCanvas(x, 0);
        ctx.beginPath();
        ctx.moveTo(px, 0);
        ctx.lineTo(px, canvas.height);
        ctx.stroke();
    }

    // Horizontal grid lines
    for (let y = yStart; y <= yEnd; y += step) {
        const { py } = worldToCanvas(0, y);
        ctx.beginPath();
        ctx.moveTo(0, py);
        ctx.lineTo(canvas.width, py);
        ctx.stroke();
    }

    ctx.restore();
}

